class Engine
{
    private String type,power,stroke;

    private Engine(String type,String power,String stroke)
    {
        this.type=type;
        this.power=power;
        this.stroke=stroke;
    }

    public String getType()
    {
        return type;
    }
    public String getPower()
    {
        return power;
    }
    public String getStroke()
    {
        return stroke;
    }






}
